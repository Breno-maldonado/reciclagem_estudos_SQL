ALTER TABLE -- alterar tabela
ALTER TABLE tabelaclientes -- seleciona a tabela a qual vai alterar
ALTER TABLE tabelaclientes ADD Endereco_Cliente VARCHAR (250); -- vai adicionar uma nova coluna e passando as especificações dela

DROP TABLE -- vai excluir uma tabela
DROP TABLE tabelaclientes -- para excluir uma tabela especifica, você passa o nome da mesma

DROP SCHEMA -- vai excluir um banco de dados inteiro
DROP SCHEMA db_financas -- para excluir um banco de dados especifico